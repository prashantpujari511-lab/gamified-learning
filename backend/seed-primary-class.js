// Seed Function
async function seedPrimaryClassTopics() {
  try {
    console.log("🌱 Starting Primary Class Topics seeding...");

    let topicCount = 0;

    // Seed Section 1 (Grades 1-2)
    for (const [subject, topics] of Object.entries(
      primaryClassTopics.section1.subjects
    )) {
      for (const topic of topics) {
        const topicId = `PRIM_S1_${subject.toUpperCase().replace(/\s+/g, "_")}_${
          topics.indexOf(topic) + 1
        }`;

        const topicDoc = new Topic({
          topicId,
          section: "foundation",
          grade: [1, 2],
          subject,
          topicName: topic.topicName,
          description: topic.description,
          learningObjective: topic.learningObjective,
          difficulty: topic.difficulty,
          xpReward: topic.xpReward,
          gameType: topic.gameType,
          microConcepts: topic.microConcepts,
          tags: topic.tags,
        });

        await topicDoc.save();
        topicCount++;
        console.log(`✅ Seeded: ${topicId} - ${topic.topicName}`);
      }
    }

    // Seed Section 2 (Grades 3-4)
    for (const [subject, topics] of Object.entries(
      primaryClassTopics.section2.subjects
    )) {
      for (const topic of topics) {
        const topicId = `PRIM_S2_${subject.toUpperCase().replace(/\s+/g, "_")}_${
          topics.indexOf(topic) + 1
        }`;

        const topicDoc = new Topic({
          topicId,
          section: "intermediate",
          grade: [3, 4],
          subject,
          topicName: topic.topicName,
          description: topic.description,
          learningObjective: topic.learningObjective,
          difficulty: topic.difficulty,
          xpReward: topic.xpReward,
          gameType: topic.gameType,
          microConcepts: topic.microConcepts,
          tags: topic.tags,
        });

        await topicDoc.save();
        topicCount++;
        console.log(`✅ Seeded: ${topicId} - ${topic.topicName}`);
      }
    }

    console.log(`\n✅ Successfully seeded ${topicCount} Primary Class Topics!`);
    mongoose.connection.close();
  } catch (error) {
    console.error("❌ Error seeding topics:", error);
    mongoose.connection.close();
    process.exit(1);
  }
}

seedPrimaryClassTopics();
